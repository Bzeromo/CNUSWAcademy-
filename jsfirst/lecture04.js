var a=5, b=3;

// 연산자와 사칙연산
console.log(a+b); // 덧셈
console.log(a-b); // 뺄셈
console.log(a+3); // 숫자 혼용 가능
console.log(5+3);
console.log(a*b); // 곱셈
console.log(a/b); // 나눗셈의 몫
console.log(a%b); // 나눗셈의 나머지
console.log(-a); // 증감연산자(++, --)와 같은 단항연산자
console.log(++a); // 증감연산자 (a= a+1;)
console.log(a); 

console.log(a++); // 증감연산자의 순서에 따른 출력값 변화 확인
console.log(a);

Math.pow(2,3); // 제곱을 구해주는 함수
Math.sqrt(16); // 제곱근을 구해주는 함수
Math.random(); // 0에서 1 사이의 난수를 반환하는 함수